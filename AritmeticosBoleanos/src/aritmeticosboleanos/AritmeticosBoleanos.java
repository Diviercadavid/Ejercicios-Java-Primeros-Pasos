
package aritmeticosboleanos;


public class AritmeticosBoleanos {


    public static void main(String[] args) {
        int n1 = 2, n2 = 2;
        
        System.out.println("El valor de n1 es = "+n1 + "      El valor de n2 es: " +n2);
        System.out.println("El resultado de n1 == n2 :  " + (n1 == n2));
        System.out.println("El resultado de n1 != n2: " + (n1 != n2));
        System.out.println("El resultado de n1 > n2: " + (n1 > n2));
        System.out.println("El resultado de n1 < n2: " + (n1 < n2));
        System.out.println("El resultado de n1 >= n2: " + (n1 >= n2));
        System.out.println("El resultado de n1 <= n2: " + (n1 <= n2));
        
    }
    
    
}
