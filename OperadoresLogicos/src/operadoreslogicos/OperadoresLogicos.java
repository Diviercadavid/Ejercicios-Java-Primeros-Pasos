
package operadoreslogicos;


public class OperadoresLogicos {

    public static void main(String[] args) {

        boolean v = true;
        boolean f = false;
        
        System.out.println("V = True      F = False");
        System.out.println("Operador AND (v&&f) = " + (v&&f));
        System.out.println("Operador OR (v||f) = " + (v||f));
        System.out.println("Operador NOT !(v && f) = " + !(v && f));
        
    }
    
}

